package com.example.ibcool.Model;

public class DayInWeek {
    String NameInLao;
    String NameInEng;
    int DayColor;

    public String getNameInLao() {
        return NameInLao;
    }

    public void setNameInLao(String nameInLao) {
        NameInLao = nameInLao;
    }

    public String getNameInEng() {
        return NameInEng;
    }

    public void setNameInEng(String nameInEng) {
        NameInEng = nameInEng;
    }

    public int getDayColor() {
        return DayColor;
    }

    public void setDayColor(int dayColor) {
        DayColor = dayColor;
    }
}
